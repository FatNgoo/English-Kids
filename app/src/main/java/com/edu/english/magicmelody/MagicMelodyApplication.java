    @Override
    public void onTerminate() {
        super.onTerminate();
        
        // Save settings
        saveSettings();
        
        // Release audio
        if (audioMixer != null) {
            audioMixer.release();
        }
        
        Log.d(TAG, "üëã Magic Melody Application Terminated");
    }
    
    @Override
    public void onLowMemory() {
        super.onLowMemory();
        Log.w(TAG, "‚ö†Ô∏è Low memory warning");
        
        // Release non-essential resources
        if (audioMixer != null) {
            audioMixer.getSoundPoolManager().unloadUnusedSounds();
        }
    }
    
    @Override
    public void onTrimMemory(int level) {
        super.onTrimMemory(level);
        
        if (level >= TRIM_MEMORY_MODERATE) {
            Log.w(TAG, "‚ö†Ô∏è Trim memory: " + level);
            if (audioMixer != null) {
                audioMixer.getSoundPoolManager().unloadUnusedSounds();
            }
        }
    }
